// ─── Fuzzy match: product name → best image filename ─────────────────────────
function normalise(str) {
  return str
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, ' ')
    .split(/\s+/)
    .filter(w => w.length > 1);
}

function scoreFuzzy(productName, imagePath) {
  const filename  = imagePath.split('/').pop().replace(/\.[^.]+$/, '');
  const prodWords = new Set(normalise(productName));
  const imgWords  = normalise(filename);
  if (!imgWords.length) return 0;
  let matches = 0;
  for (const w of imgWords) if (prodWords.has(w)) matches++;
  return matches / imgWords.length;
}

function findBestImage(productName) {
  let best = null, bestScore = 0;
  for (const img of IMAGE_PATHS) {
    const score = scoreFuzzy(productName, img);
    if (score > bestScore) { bestScore = score; best = img; }
  }
  return bestScore >= 0.5 ? { path: best, score: bestScore } : null;
}

// ─── WooCommerce helpers ──────────────────────────────────────────────────────
async function getAllProducts() {
  let page = 1, all = [];
  console.log('📦 Fetching all WooCommerce products...');
  while (true) {
    const res = await fetch(
      `${BASE_URL}/wp-json/wc/v3/products?per_page=100&page=${page}&status=publish`,
      { headers: { Authorization: AUTH } }
    );
    if (!res.ok) { console.error(`API error: ${res.status}`); break; }
    const products = await res.json();
    if (!products.length) break;
    all = all.concat(products);
    console.log(`  Page ${page}: ${products.length} products (total: ${all.length})`);
    page++;
    if (products.length < 100) break;
  }
  return all;
}

async function updateProductImage(id, imageUrl) {
  const res = await fetch(`${BASE_URL}/wp-json/wc/v3/products/${id}`, {
    method: 'PUT',
    headers: { Authorization: AUTH, 'Content-Type': 'application/json' },
    body: JSON.stringify({ images: [{ src: imageUrl }] }),
  });
  if (!res.ok) throw new Error(`${res.status}: ${await res.text()}`);
  return res.json();
}

// ─── Main ─────────────────────────────────────────────────────────────────────
async function main() {
  const products = await getAllProducts();
  const noImage  = products.filter(p => !p.images || !p.images.length);
  const hasImage = products.filter(p => p.images && p.images.length);

  console.log(`\n✅ Total products: ${products.length}`);
  console.log(`🖼  Already have images: ${hasImage.length}`);
  console.log(`❌ Missing images:       ${noImage.length}\n`);

  if (!noImage.length) { console.log('🎉 All products already have images!'); return; }

  let updated = 0, failed = 0, unmatched = 0;
  const unmatchedList = [];

  for (const product of noImage) {
    const match = findBestImage(product.name);
    if (!match) {
      unmatched++;
      unmatchedList.push(product.name);
      continue;
    }

    const imageUrl = `${BASE_URL}/wp-content/uploads/${match.path}`;
    process.stdout.write(`  [${product.id}] ${product.name.substring(0, 45).padEnd(45)} → ${match.path.split('/').pop()}`);

    try {
      await updateProductImage(product.id, imageUrl);
      updated++;
      process.stdout.write(' ✅\n');
    } catch (err) {
      failed++;
      process.stdout.write(` ❌ ${err.message}\n`);
    }

    await new Promise(r => setTimeout(r, 150)); // avoid rate limiting
  }

  console.log('\n─────────────────────────────────');
  console.log(`✅ Updated:       ${updated}`);
  console.log(`❌ API errors:    ${failed}`);
  console.log(`⚠️  No match:     ${unmatched}`);

  if (unmatchedList.length) {
    console.log('\n📋 No match found for:');
    unmatchedList.forEach(n => console.log(`  • ${n}`));
  }
}

main().catch(console.error);
